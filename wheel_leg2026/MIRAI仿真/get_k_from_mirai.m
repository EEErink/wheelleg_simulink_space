    % 计算所有 40 个 K 元素
    K_vec = K_fit_coeffs * basis(:);  % (40×6) × (6×1) → (40×1)

    % 重塑为 4×10（注意：原拟合时是按 K(1,1), K(1,2), ..., K(4,10) 展平的）
    % 假设你之前是按列优先（reshape(K.', [1,40])），则还原方式如下：
    K = reshape(K_vec, [10, 4]).';  % 先转为 10×4，再转置为 4×10
end