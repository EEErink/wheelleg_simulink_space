function K = eval_K_from_leg_lengths(l_l, l_r, K_fit_coeffs)
%EVAL_K_FROM_LEG_LENGTHS 根据拟合系数和腿长计算 LQR 增益矩阵 K
%   l_l: 左腿长度 (scalar)
%   l_r: 右腿长度 (scalar)
%   K_fit_coeffs: 40×6 矩阵，每行对应一个 K_ij 的 [c0, c1, c2, c3, c4, c5]
%   返回: K (4×10)

    if size(K_fit_coeffs, 1) ~= 40 || size(K_fit_coeffs, 2) ~= 6
        error('K_fit_coeffs 必须是 40×6 的矩阵');
    end

    % 构造基函数 [1, l_l, l_r, l_l^2, l_l*l_r, l_r^2]
    basis = [1, l_l, l_r, l_l^2, l_l*l_r, l_r^2];

    % 计算所有 40 个 K 元素
    K_vec = K_fit_coeffs * basis(:);  % (40×6) × (6×1) → (40×1)

    % 重塑为 4×10（注意：原拟合时是按 K(1,1), K(1,2), ..., K(4,10) 展平的）
    % 假设你之前是按列优先（reshape(K.', [1,40])），则还原方式如下：
    K = reshape(K_vec, [10, 4]).';  % 先转为 10×4，再转置为 4×10
end